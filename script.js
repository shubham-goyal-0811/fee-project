document.addEventListener('scroll', function () {
    const scrollPosition = window.scrollY;
    const parallaxItems = document.querySelectorAll('.parallax-item');
    const parallaxItemBottom = document.querySelector('.parallax-item-bottom');
    const parallaxItemTop = document.querySelector('.parallax-item-top');

    parallaxItems.forEach(function (item, index) {
        const speed = (index + 1) * 1;
        item.style.transform = `translateX(${scrollPosition * speed}px)`;
    });

    parallaxItemBottom.style.transform = `translateY(-${scrollPosition * 0.3}px)`;
    parallaxItemTop.style.transform = `translateX(-${scrollPosition * 0.3}px)`;
});

